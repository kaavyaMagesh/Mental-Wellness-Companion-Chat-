-- MODULE: Database Schema Draft (Sprint 1, Day 1)
-- DESCRIPTION: Draft schema definition covering users, mood_entries, 
--              journal_entries, chat_sessions, chat_messages, assessments, 
--              support_requests, crisis_flags, wellness_resources, and 
--              resource_embeddings tables.
